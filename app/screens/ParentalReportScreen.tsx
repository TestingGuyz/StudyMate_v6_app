import React, { useState, useEffect } from 'react';
import { View, Text, StyleSheet, Platform, ScrollView, TouchableOpacity, ActivityIndicator, Alert } from 'react-native';
import { router } from 'expo-router';
import { Ionicons } from '@expo/vector-icons';
import * as Linking from 'expo-linking';
import { useTheme, useAuth } from '../../lib/context';
import { buildStudentContext } from '../../lib/adaptiveEngine';
import { callGroq } from '../../lib/groq';

export default function ParentalReportScreen() {
  const { colors } = useTheme();
  const { studentId } = useAuth();
  const [report, setReport] = useState<string>('');
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    (async () => {
      if (!studentId) return;
      try {
        const contextStr = await buildStudentContext(studentId);
        
        const prompt = `You are an AI academic advisor. Generate a professional, empathetic "Parental Summary Report" based on the following student data.
Format it nicely but do NOT use Markdown (no asterisks or hash symbols) because this will be put into an email body in plain text.
Include:
1. A brief overview of their recent study habits and time spent.
2. Areas where they are excelling (Strong Subjects).
3. Areas requiring more attention (Weak Subjects).
4. Their emotional well-being based on recent mood logs.
5. One actionable tip for the parent to support them.

Student Data:
${contextStr}`;

        const response = await callGroq([
          { role: 'system', content: 'You are a professional educational counselor generating plain-text reports for parents.' },
          { role: 'user', content: prompt }
        ], 'report_generation');
        
        // Clean up any stray markdown
        const plainText = response.replace(/\*\*/g, '').replace(/\*/g, '').replace(/#/g, '');
        setReport(plainText);
      } catch (err) {
        console.error(err);
        setReport("Error generating report. Please check your AI API key and connection.");
      } finally {
        setLoading(false);
      }
    })();
  }, [studentId]);

  const sendEmail = async () => {
    if (!report) return;
    const subject = encodeURIComponent("StudyMate AI - Weekly Academic & Well-being Report");
    const body = encodeURIComponent(report + "\n\n---\nGenerated by StudyMate AI");
    const mailtoUrl = `mailto:?subject=${subject}&body=${body}`;
    
    try {
      const canOpen = await Linking.canOpenURL(mailtoUrl);
      if (canOpen || Platform.OS === 'web') {
        await Linking.openURL(mailtoUrl);
      } else {
        Alert.alert("Error", "No email app configured on this device.");
      }
    } catch (e) {
      Alert.alert("Error", "Could not open email app.");
    }
  };

  return (
    <View style={[styles.container, { backgroundColor: colors.background }]}>
      <View style={styles.header}>
        <TouchableOpacity onPress={() => router.back()}>
          <Ionicons name="arrow-back" size={24} color={colors.text} />
        </TouchableOpacity>
        <Text style={{color: colors.text, fontSize: 18, fontWeight: '600'}}>Parental Report Card</Text>
        <View style={{width: 24}} />
      </View>

      <ScrollView contentContainerStyle={styles.content}>
        <View style={[styles.iconWrap, { backgroundColor: colors.primary + '15' }]}>
          <Ionicons name="mail-unread-outline" size={48} color={colors.primary} />
        </View>
        <Text style={[styles.title, { color: colors.text }]}>Weekly AI Summary</Text>
        <Text style={[styles.subtitle, { color: colors.textSecondary }]}>
          A comprehensive overview of academic performance and emotional well-being to share with parents.
        </Text>

        <View style={[styles.reportCard, { backgroundColor: colors.surface, borderColor: colors.border }]}>
          {loading ? (
            <View style={{ padding: 40, alignItems: 'center' }}>
              <ActivityIndicator size="large" color={colors.primary} />
              <Text style={{ marginTop: 16, color: colors.textTertiary }}>Analyzing student behavioral graph...</Text>
            </View>
          ) : (
            <Text style={[styles.reportText, { color: colors.text }]}>{report}</Text>
          )}
        </View>

        {!loading && (
          <TouchableOpacity 
            style={[styles.emailBtn, { backgroundColor: colors.primary }]}
            onPress={sendEmail}
          >
            <Ionicons name="send" size={20} color={colors.onPrimary} />
            <Text style={{ color: colors.onPrimary, fontWeight: '700', fontSize: 16 }}>Send via Gmail / Email</Text>
          </TouchableOpacity>
        )}
      </ScrollView>
    </View>
  );
}

const styles = StyleSheet.create({
  container: { flex: 1, paddingTop: Platform.OS === 'ios' ? 50 : 30 },
  header: { paddingHorizontal: 20, paddingVertical: 10, flexDirection: 'row', justifyContent: 'space-between', alignItems: 'center' },
  content: { padding: 20 },
  iconWrap: { width: 80, height: 80, borderRadius: 40, alignItems: 'center', justifyContent: 'center', marginBottom: 20, alignSelf: 'center' },
  title: { fontSize: 24, fontWeight: '700', marginBottom: 8, textAlign: 'center' },
  subtitle: { fontSize: 14, lineHeight: 20, marginBottom: 24, textAlign: 'center' },
  reportCard: { padding: 20, borderRadius: 16, borderWidth: 1, marginBottom: 24 },
  reportText: { fontSize: 15, lineHeight: 26 },
  emailBtn: { flexDirection: 'row', alignItems: 'center', justifyContent: 'center', gap: 10, padding: 16, borderRadius: 12 }
});
